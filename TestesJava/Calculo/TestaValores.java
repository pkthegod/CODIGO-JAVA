package Calculo;

public class TestaValores {
    public static void main(String[] args) {
        int valor1=10, valor2=5;

        if(valor1>valor2) System.out.println("valor1 é maior a valor2");
        if(valor1<valor2) System.out.println("valor1 é menor valor2");
        if(valor1==valor2) System.out.println("valor1 é igual a valor2");
        if(valor1>=valor2) System.out.println("valor1 é maior ou igual a valor2");
        if(valor1<=valor2) System.out.println("valor1 é menor ou igual a valor2");
        if(valor1!=valor2) System.out.println("valor1 não é valor2");
    }
    
}
